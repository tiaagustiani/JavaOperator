public class OperatorRelasional {
	public static void main(String[] args) {
		int x = 5, y = 6;
		System.out.println(x);
		System.out.println(y);
		System.out.println(x=y);
		System.out.println(x==y);
		System.out.println();
		System.out.println(1 < 2);
		System.out.println(1 > 2);
		System.out.println(2 <= 2);
		System.out.println(3 >= 5);
		System.out.println(3 != 5);
		System.out.println(3 == 5);
	}
}
